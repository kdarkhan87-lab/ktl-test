// Helper: get module by id
function getModuleById(moduleId) {
    for (const mod of CURRICULUM.grade5) {
        if (mod.id === moduleId) return mod;
    }
    return null;
}

// Helper: get topic by ids
function getTopicById(moduleId, topicId) {
    const mod = getModuleById(moduleId);
    if (!mod) return null;
    return mod.topics.find(t => t.id === topicId) || null;
}

// Get modules by grade filter
function getModulesByGrade(grade) {
    return CURRICULUM.grade5.filter(m => String(m.grade) === String(grade));
}
